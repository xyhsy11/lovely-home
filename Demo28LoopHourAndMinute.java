public class Demo28LoopHourAndMinute {
	public static void main(String[] args) {
		int i = 0;
		for (int hour = 0; hour < 24; hour++) {
			
			
			for (int minute = 0; minute < 60; minute ++){
				i++;
				System.out.println(hour + "hour" + minute + "minute"+" i= :"+i);
			}
			
			
			
			
			
			
			
			
			
			
			
			
			
			
		}
	}
}